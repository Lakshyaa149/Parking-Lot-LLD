import java.util.List;

public class ParkingSlots {
    List<Slot> bikeSlots;
    List<Slot> cycleClots;
    List<Slot> carSlots;

    public List<Slot> getBikeSlots() {
        return bikeSlots;
    }

    public void setBikeSlots(List<Slot> bikeSlots) {
        this.bikeSlots = bikeSlots;
    }

    public List<Slot> getCycleClots() {
        return cycleClots;
    }

    public void setCycleClots(List<Slot> cycleClots) {
        this.cycleClots = cycleClots;
    }

    public List<Slot> getCarSlots() {
        return carSlots;
    }

    public void setCarSlots(List<Slot> carSlots) {
        this.carSlots = carSlots;
    }




}
